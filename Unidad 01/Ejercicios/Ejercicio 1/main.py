# **1) Identifica el tipo de dato (int, float, string o list) de los siguientes valores literales**
# ```python
# "Hola Mundo" 
# [1, 10, 100]
# -25          
# 1.167       
# ["Hola", "Mundo"]  
# ' '        
# ```


if __name__ == '__main__': 
    print(type("Hola Mundo"))
    print(type([1, 10, 100]))
    print(type(-25))
    print(type(1.167))
    print(type(["Hola", "Mundo"]  ))
    print(type(' '))